package com.ahmer.afzal.pdfium;

public class Meta {

    String title;
    String author;
    String subject;
    String keywords;
    String creator;
    String producer;
    String creationDate;
    String modDate;
    int totalPages;

    public int getTotalPages() {
        return totalPages;
    }

    public String getTitle() {
        return title;
    }

    public String getAuthor() {
        return author;
    }

    public String getSubject() {
        return subject;
    }

    public String getKeywords() {
        return keywords;
    }

    public String getCreator() {
        return creator;
    }

    public String getProducer() {
        return producer;
    }

    public String getCreationDate() {
        return creationDate;
    }

    public String getModDate() {
        return modDate;
    }
}
